public class StampEventSearchForm {

	private String stampEventId;
	private String stampId;
	private String eventSpace;
	private String eventContents;
	private String deleteFlg;


	public void setStampEventId(String stampEventId) {
		this.stampEventId = stampEventId;
	}


	public void setStampId(String stampId) {
		this.stampId = stampId;
	}


	public void setEventSpace(String eventSpace) {
		this.eventSpace = eventSpace;
	}


	public void setEventContents(String eventContents) {
		this.eventContents = eventContents;
	}


	public void setDeleteFlg(String deleteFlg) {
		this.deleteFlg = deleteFlg;
	}


	public String getStampEventId() {
		return stampEventId;
	}


	public String getStampId() {
		return stampId;
	}


	public String getEventSpace() {
		return eventSpace;
	}


	public String getEventContents() {
		return eventContents;
	}


	public String getDeleteFlg() {
		return deleteFlg;
	}
	
}
